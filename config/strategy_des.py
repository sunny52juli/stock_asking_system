"""
策略描述配置模块

提供已验证有效的策略模板，用于指导 LLM 生成筛选策略。

================================================================================
策略配置说明（面向非技术用户）：
================================================================================

用户只需做两件事：
1. 用自然语言描述策略（demo_query）
2. 设置要生成的策略数量（strategy_num）

Agent 会自动：
- 理解你的策略描述
- 选择合适的工具和指标
- 生成指定数量的策略文件

示例：
    STRATEGY_TEMPLATES = {
        "我的策略": {
            "description": "用自然语言描述你的策略想法",
            "demo_query": "找出...的股票",
            "strategy_num": 3,  # 让 Agent 生成 3 个不同版本的策略
        },
    }
================================================================================
"""


class StrategyDescriptions:
    """策略描述配置类 - 仅包含实际有效的策略模板"""
    
    # ==================== 已验证的策略模板 ====================
    # 说明：只保留实际运行过、有回测数据的策略
    # 
    # 配置字段说明：
    #   description: 策略功能描述（中文，给用户看）
    #   demo_query: 用户输入的查询示例（自然语言）
    #   strategy_num: 让 Agent 生成几个不同版本的策略（默认 1）
    # ==========================================================
    STRATEGY_TEMPLATES = {
        "放量突破": {
            "description": "筛选成交量放大且价格突破的股票",
            "demo_query": """找出最近放量突破的股票：
    1. 成交量较前期放大（至少 1.5 倍）
    2. 涨幅>3%
    3. 技术形态良好""",
            "strategy_num": 1,  # 生成 1 个策略文件
        },
    }
    
    # ==================== 策略生成配置 ====================
    # 用于 run.py 策略生成时的数据加载和筛选
    SCREENING_DATE: str = "20260320"  # 格式：YYYYMMDD（确保前有至少 80 个交易日）
    OBSERVATION_PERIOD_DAYS: int = 80  # 观察期长度 (交易日)，用于技术指标计算
    
    @classmethod
    def get_demo_queries(cls) -> list[str]:
        """获取演示查询列表（从策略模板中提取）"""
        return [
            template["demo_query"]
            for template in cls.STRATEGY_TEMPLATES.values()
            if "demo_query" in template
        ]
